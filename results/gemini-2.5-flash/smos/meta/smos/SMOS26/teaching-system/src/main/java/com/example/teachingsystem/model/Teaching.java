package com.example.teachingsystem.model;

import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

/**
 * Represents a Teaching entity in the system.
 * This class is mapped to a database table using JPA.
 */
@Entity // Marks this class as a JPA entity
@Data // Lombok annotation to generate getters, setters, toString, equals, and hashCode methods
@NoArgsConstructor // Lombok annotation to generate a no-argument constructor
@AllArgsConstructor // Lombok annotation to generate a constructor with all fields
public class Teaching {

    /**
     * The unique identifier for the teaching.
     * It is the primary key and is automatically generated.
     */
    @Id // Marks this field as the primary key
    @GeneratedValue(strategy = GenerationType.IDENTITY) // Configures the primary key to be auto-generated by the database
    private Long id;

    /**
     * The name of the teaching.
     */
    private String name;

    /**
     * A detailed description of the teaching.
     */
    private String description;

    // Lombok's @Data, @NoArgsConstructor, and @AllArgsConstructor handle the generation
    // of constructors, getters, and setters, fulfilling the design requirements.
    // Explicit constructors are not needed if Lombok is used.
}