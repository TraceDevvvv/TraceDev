package com.example.account;

import java.util.Map;
import java.util.HashMap;

/**
 * Implementation of the IAccountRepository interface.
 * Acts as an adapter to the actual data storage mechanism (DatabaseAdapter).
 */
public class AccountRepositoryImpl implements IAccountRepository {
    private final DatabaseAdapter databaseAdapter;

    /**
     * Constructor for AccountRepositoryImpl.
     *
     * @param databaseAdapter The adapter responsible for low-level database operations.
     */
    public AccountRepositoryImpl(DatabaseAdapter databaseAdapter) {
        this.databaseAdapter = databaseAdapter;
    }

    /**
     * Saves an Account object to the database.
     *
     * @param account The Account object to save.
     * @return The saved Account object, potentially with an updated ID if generated by DB.
     * @throws DatabaseException if there's an issue with database operation.
     */
    @Override
    public Account save(Account account) {
        // Convert Account entity to a Map suitable for the DatabaseAdapter
        Map<String, String> accountData = new HashMap<>();
        accountData.put("id", account.getId());
        accountData.put("username", account.getUsername());
        accountData.put("email", account.getEmail());
        accountData.put("passwordHash", account.getPasswordHash());

        // Call the database adapter to insert
        String newAccountId = databaseAdapter.insertAccount(accountData);

        // If the database generated a new ID, update the account object (or return a new one).
        // For this example, we assume the ID is already set on the Account object passed in.
        // If newAccountId is different, we might construct a new Account or set the ID.
        if (newAccountId != null && !newAccountId.equals(account.getId())) {
            // This scenario implies DB might override or generate IDs.
            // For simplicity, we'll assume account.getId() is used as the primary key.
            System.out.println("Warning: DatabaseAdapter returned a different ID. Using original: " + account.getId());
        }
        return account; // Return the original account as it's assumed to be updated in place or ID matched.
    }

    /**
     * Finds an Account object by its username in the database.
     *
     * @param username The username to search for.
     * @return The Account object if found, or null otherwise.
     * @throws DatabaseException if there's an issue with database operation.
     */
    @Override
    public Account findByUsername(String username) {
        Map<String, String> accountData = databaseAdapter.findAccountByUsername(username);
        if (accountData != null && !accountData.isEmpty()) {
            // Convert Map from DatabaseAdapter back to an Account entity
            return new Account(
                    accountData.get("id"),
                    accountData.get("username"),
                    accountData.get("email"),
                    accountData.get("passwordHash")
            );
        }
        return null;
    }
}