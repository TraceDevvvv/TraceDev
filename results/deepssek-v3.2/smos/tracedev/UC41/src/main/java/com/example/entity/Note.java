package com.example.entity;

import com.example.dto.NoteUpdateRequest;
import java.util.Date;

/**
 * Entity class representing a Note.
 */
public class Note {
    private int id;
    private String student;
    private String description;
    private String teacher;
    private Date date;
    private String createdBy;
    private String modifiedBy;
    private Date createdAt;
    private Date updatedAt;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getStudent() {
        return student;
    }

    public void setStudent(String student) {
        this.student = student;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getTeacher() {
        return teacher;
    }

    public void setTeacher(String teacher) {
        this.teacher = teacher;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(String createdBy) {
        this.createdBy = createdBy;
    }

    public String getModifiedBy() {
        return modifiedBy;
    }

    public void setModifiedBy(String modifiedBy) {
        this.modifiedBy = modifiedBy;
    }

    public Date getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Date createdAt) {
        this.createdAt = createdAt;
    }

    public Date getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Date updatedAt) {
        this.updatedAt = updatedAt;
    }

    public void updateDetails(NoteUpdateRequest request) {
        this.setStudent(request.getStudent());
        this.setDescription(request.getDescription());
        this.setTeacher(request.getTeacher());
        this.setDate(request.getDate());
        this.setModifiedBy(request.getModifiedBy());
    }
}